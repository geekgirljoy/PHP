<?php


class Squirrel3{
    const BIT_NOISE1 = 0xB5297A4D;//B5297A4D;
    const BIT_NOISE2 = 0x68E31DA4;//68E31DA4;
    const BIT_NOISE3 = 0x1B56C4E9;//1B56C4E9;

    const LARGE_PRIME_NUMBER_1 = 198491317;
    const LARGE_PRIME_NUMBER_2 = 6542989;

    private $seed;

    public function __construct(){
 
    }

    public function GetSeed(){
        return $this->seed;
    }

    public function SetSeed($seed){
        $this->seed = $seed;
    }

    public function GetNoise1D(int $position){
        $mangled = $position;
        $mangled *= self::BIT_NOISE1;
        $mangled += $this->seed;
        $mangled ^= ($mangled >> 8);
        $mangled += self::BIT_NOISE2;
        $mangled ^= ($mangled << 8);
        $mangled *= self::BIT_NOISE3;
        $mangled ^= ($mangled >> 8);
        return $mangled; // return the hash
    }

    public function GetNoise2D(int $position_x, int $position_y){
        return $this->GetNoise1D($position_x + (self::LARGE_PRIME_NUMBER_1 * $position_y));
    }

    public function GetNoise3D(int $position_x, int $position_y, int $position_z){
        return $this->GetNoise1D($position_x + (self::LARGE_PRIME_NUMBER_1 * $position_y) + (self::LARGE_PRIME_NUMBER_2 * $position_z));
    }
}

